import Foundation

enum ChordType: String {
    case cMajor = "C Major"
    case cMinor = "C Minor"
    case dMajor = "D Major"
    case dMinor = "D Minor"
    case eMajor = "E Major"
    case eMinor = "E Minor"
    case fMajor = "F Major"
    case fMinor = "F Minor"
    case gMajor = "G Major"
    case gMinor = "G Minor"
    case aMajor = "A Major"
    case aMinor = "A Minor"
    case bMajor = "B Major"
    case bMinor = "B Minor"
}
